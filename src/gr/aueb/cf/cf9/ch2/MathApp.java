/**
 * Math Demo. In Java there is a Math Class.
 */
public class MathApp {

    public static void main(String[] args) {
        System.out.println("Το μικρότερο μεταξύ 3 και 5 είναι: " + Math.min(3, 5));
        System.out.println("Το μεγαλύτερο μεταξύ 3 και 5 είναι: " + Math.max(3, 5));
    }

}
